function mergeLocale(overrides: Record<string, unknown>) {
  const en = translations.en;
  return {
    ...en,
    ...overrides,
    header: { ...en.header, ...(overrides.header as object) },
    hero: { ...en.hero, ...(overrides.hero as object) },
    productList: { ...en.productList, ...(overrides.productList as object) },
    gems: { ...en.gems, ...(overrides.gems as object) },
  };
}

export function getTranslations(lang: Language) {
  if (lang === 'fr') return mergeLocale(frOverrides);
  if (lang === 'ar') return ar;
  return translations[LANG_TO_TRANSLATION[lang]];
}
